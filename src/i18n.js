export function useT(lang) {
  const s = STRINGS[lang] || STRINGS.en;
  return (key, vars) => {
    let str = s[key] || key;
    if (vars) Object.keys(vars).forEach(k => { str = str.replace(`{${k}}`, vars[k]); });
    return str;
  };
}
